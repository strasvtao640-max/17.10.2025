def sum_array(arr):
    # Базовый случай: если массив пуст — возвращаем 0
    if len(arr) == 0:
        return 0

    # Рекурсивно: первый элемент + сумма остальных
    return arr[0] + sum_array(arr[1:])


# ==== запуск программы ====
# Ввод массива от пользователя
nums = input("Введите числа через пробел: ")

# Преобразуем строку в список чисел
arr = list(map(int, nums.split()))

# Вызываем рекурсивную функцию
result = sum_array(arr)

# Выводим результат
print("Сумма элементов:", result)
